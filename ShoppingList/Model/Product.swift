import UIKit

struct Product {
    var name: String?
    var amount: Double?
    var number: Int?
    var size: String? //Small-Meduim-Large
    var describe: String?
    
    func getName(name: String) -> String {
        
        return name
    }
    func getAmount(amount: Double) -> Double {
        
        return amount
    }
    func getNumber(number: Int) -> Int {
        
        return number
    }
    func getSize(size: String) -> String {
        
        return size
    }
    func getDescribe(describe: String) -> String {
        
        return describe
    }
    
    
    
    
    
    
}
